// Used to fetch all themes from the Dice Box
export async function fetchDiceThemes(dddice: ThreeDDice) {
  const totalThemes: ITheme[] = [];

  // Fetch all themes
  let res = await dddice.api?.diceBox.list();
  totalThemes.push(...(res?.data ?? []));
  // @ts-ignore
  while (res?.links?.next) {
    res = (await dddice.api?.diceBox.next()) ?? undefined;
    totalThemes.push(...(res?.data ?? []));
  }

  // Convert themes
  return (
    totalThemes
      // We assume that 6 dice means it's a full set d4-d20
      .filter((theme) => theme.available_dice.length >= 6)
      .map((theme) => {
        const images: { d4: string; d6: string; d8: string; d10: string; d12: string; d20: string } = {
          d4: '',
          d6: '',
          d8: '',
          d10: '',
          d12: '',
          d20: '',
        };
        for (const dice of theme.available_dice) {
          // @ts-ignore
          images[dice.notation] = theme.preview[dice.id];
        }

        let name = theme.name ?? 'Unknown';
        name = toLabel(name).split('(')[0].trim();
        if (name.trim().endsWith('Dice')) {
          name = name.replace('Dice', '').trim();
        }
        return {
          name: name,
          theme: theme.id,
          preview: images,
        };
      })
      .filter(
        (theme) =>
          theme.preview.d4 &&
          theme.preview.d6 &&
          theme.preview.d8 &&
          theme.preview.d10 &&
          theme.preview.d12 &&
          theme.preview.d20
      )
  );
}

export function findDiceTheme(theme: string): DiceTheme {
  return DICE_THEMES.find((diceTheme) => diceTheme.theme === theme) || DICE_THEMES[0];
}
